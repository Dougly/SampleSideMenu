import UIKit

extension UIColor {
    
    static var themeGreen: UIColor {
        return UIColor(colorLiteralRed: 0/255, green: 233/255, blue: 145/255, alpha: 1)
    }
    
    static var themeBlue: UIColor {
        return UIColor(colorLiteralRed: 0/255, green: 193/255, blue: 249/255, alpha: 1)
    }
    
}
